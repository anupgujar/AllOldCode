package firstDecPackage;

public class Constructor {
	int id;
	String className;
	String collegeName;

	public Constructor(int id, String className, String collegeName) {
		this.id = id;
		this.className = className;
		this.collegeName = collegeName;
		System.out.println("3 global Variable");
	}
	public Constructor(int id, String className) {
		this.id = id;
		this.className = className;
		System.out.println("2 global variable");
	}
	public Constructor(int id) {
		this.id = id;
		System.out.println("1 global variable");
	}
	
	public static void main(String[] args) {
		Constructor s1 = new Constructor(12, "FifthClass", "SSB College");
		Constructor s2 = new Constructor(23, "tenth Class");
		Constructor s3 = new Constructor(54);
		
	}
}
