package firstDecPackage;

import java.util.Scanner;

public class Months {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int num = sc.nextInt();
		
		switch(num){
		case 1 : System.out.println("The name of month is January");
		break;
		case 2 : System.out.println("The name of month is February");
		break;
		case 3 : System.out.println("The name of month is March");
		break;
		case 4 : System.out.println("The name of month is April");
		break;
		case 5 : System.out.println("The name of month is May");
		break;
		case 6 : System.out.println("The name of month is June");
		break;
		case 7 : System.out.println("The name of month is July");
		break;
		case 8 : System.out.println("The name of month is August");
		break;
		case 9 : System.out.println("The name of month is Septeber");
		break;
		case 10 : System.out.println("The name of month is October");
		break;
		case 11 : System.out.println("The name of month is November");
		break;
		case 12 : System.out.println("The name of month is December");
		break;
		default : System.out.println("Invalid Input");
		}
		
		
		
		
		
	}

}
